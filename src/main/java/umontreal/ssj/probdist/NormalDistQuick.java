/**
 * Same as  {@link #inverseF(double,double,double) inverseF(0.0, 1.0, u)}.
 */
public static double inverseF01 (double u) {

       /* Method of Marsaglia. REF : ? */
 
        int     n, k, j;
        double  v, x, w;
        boolean negatif;

        if (u <= 0.0)
           return Double.NEGATIVE_INFINITY;
        if (u >= 1.0)
           return Double.POSITIVE_INFINITY;
/*        if (u >= 1.0)
           return XBIG;
        if (u <= 0.0)
           return -XBIG;  */
        if (u < 0.5) {
           negatif = true;
           u = 2.0 * u;
        } else {
           negatif = false;
           u = 2.0 * (1.0 - u);
        }

        k = 0;
        w = u;
        while (w < 0.5) {
           ++k;
           w *= 2.0;
        }
        j = (int) ((w - 0.5) * 64.0);
        n = 992 - 32 * k + j;
        if (n < 0) {
           if (negatif)
              return -XBIG;
           else
              return XBIG;
        }
        v = ((int) (Num.TWOEXP[k + 6] * u)) / Num.TWOEXP[k + 6];
        v = (u - v) * B[n];

        x = A[n] - v * (1.414214 - v * (A[n] - 0.4714045 * (1.0 +
                 2.0 * A[n] * A[n]) * v));
        if (negatif)
           return -x;
        else
           return x;

   }

   /**
    * Returns an approximation of @f$\Phi^{-1}(u)@f$, where @f$\Phi@f$
    * is the standard normal distribution function, with mean 0 and
    * variance 1. Uses the method of Marsaglia, Zaman, and Marsaglia
    * @cite rMAR94b&thinsp;, with table lookups. Returns 6 decimal digits
    * of precision. This method is approximately 20% faster than
    * `NormalDist.inverseF`.
    */
   public static double inverseF (double mu, double sigma, double u) {
      if (sigma <= 0)
         throw new IllegalArgumentException ("sigma <= 0");
      return mu + sigma*inverseF01 (u);
   }
 
